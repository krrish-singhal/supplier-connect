export const getSuppliersByRegion = (region: string): Supplier[] => {
  if (region === 'all') return mockSuppliers;
  return mockSuppliers.filter((s) => s.region === region);
};

export const getSupplierById = (id: string): Supplier | undefined => {
  return mockSuppliers.find((s) => s.id === id);
};

export const getOpportunityById = (id: string): Opportunity | undefined => {
  return mockOpportunities.find((o) => o.id === id);
};
